var konamiCode = [38, 38, 40, 40, 37, 39, 37, 39, 66, 65]

var konamiCodeStatut = ["KO", "KO", "KO", "KO", "KO", "KO", "KO", "KO", "KO", "KO"]

var haroldMap = "http://i0.kym-cdn.com/entries/icons/mobile/000/016/546/hidethepainharold.jpg";

var haroldBurreau = "https://ih0.redbubble.net/image.225771333.5422/flat,800x800,075,t.jpg";

var haroldBackGround = "https://ih1.redbubble.net/image.225771333.5422/flat_pencil_skirt,x1147-bg,ffffff.jpg";
$("body").keyup(function(e){
	
	for(i = 0; i < konamiCodeStatut.length; i++){
		if(konamiCodeStatut[i] == "KO"){
			console.log(e.keyCode + " ==== " + konamiCode[i]);
			if(e.keyCode == konamiCode[i]){
				konamiCodeStatut[i] = "OK";
				console.log(konamiCodeStatut)
				if(checkAllOK()){
					haroldMaster();
				}
				break;
			}
			else {
				console.log(konamiCodeStatut)
				allResetKO();
			}
		}
	}
	
	
	
})

function allResetKO(){
	for(i = 0; i < konamiCodeStatut.length; i++){
		konamiCodeStatut[i] = "KO";
	}
}

function checkAllOK(){
	result = true;

	for(i = 0; i < konamiCodeStatut.length; i++){
		if(konamiCodeStatut[i] == "KO"){
			result = false;
		}
	}
	
	return result;
	
}

function haroldMaster(){
	$("#img").attr("src", haroldMap);

	$(".emplacement").each(function(){
		$(this).css("background-image", "url(" + haroldBurreau + ")");
		$(this).css("background-size", "20px 20px");
		$(this).css("border", "2px solid black");
	})
	
	$("body").css("background-image", "url(" + haroldBackGround+ ")");
	$("body").css("background-size", "100px 100px");
	
}